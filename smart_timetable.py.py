from langchain_google_genai import ChatGoogleGenerativeAI
from langchain_core.tools import tool
from langchain_core.messages import HumanMessage, SystemMessage


llm = ChatGoogleGenerativeAI(
    model='gemini-1.5-flash',
    google_api_key=os.environ['GEMINI_API_KEY'],
    temperature=0.3,
)


from langchain_core.tools import StructuredTool

lc_tools = [
    StructuredTool.from_function(func=tool_view_schedule,    name='ViewSchedule',    description='View timetable. Input: day name e.g. Monday, or empty string for full week.'),
    StructuredTool.from_function(func=tool_add_event,        name='AddEvent',        description='Add event with conflict detection. Input JSON string: {"day":"Monday","subject":"Study","start":"17:00","end":"18:00"}'),
    StructuredTool.from_function(func=tool_find_free_slots,  name='FindFreeSlots',   description='Find free time slots on a day. Input: day name e.g. Monday.'),
    StructuredTool.from_function(func=tool_view_assignments, name='ViewAssignments', description='List all pending assignments with urgency. Input: empty string.'),
    StructuredTool.from_function(func=tool_add_assignment,   name='AddAssignment',   description='Add assignment. Input JSON string: {"subject":"Math HW","deadline":"2026-06-28","priority":"High"}'),
    StructuredTool.from_function(func=tool_suggest_study_plan, name='SuggestStudyPlan', description='Generate a study plan matching assignments to free slots. Input: empty string.'),
]

llm_with_tools = llm.bind_tools(lc_tools)


import json

def run_agent(user_input: str, verbose: bool = True) -> str:
    """Run the tool-calling agent loop."""
    messages = [
        SystemMessage(content=(
            "You are a Smart Timetable Assistant helping a student manage "
            "their academic schedule. Use the available tools to answer questions "
            "about the timetable, find free slots, track assignments, and suggest study plans."
        )),
        HumanMessage(content=user_input),
    ]

    for i in range(6):
        response = llm_with_tools.invoke(messages)
        messages.append(response)

        if not response.tool_calls:
            if verbose:
                print(f' Iterations used: {i+1}')
            return response.content


        from langchain_core.messages import ToolMessage
        for tc in response.tool_calls:
            tool_name = tc['name']
            tool_args = tc['args']


            matched = next((t for t in lc_tools if t.name == tool_name), None)
            if matched:

                arg_value = list(tool_args.values())[0] if tool_args else ''
                result = matched.func(arg_value)
            else:
                result = f'Tool {tool_name} not found.'

            if verbose:
                print(f' Tool: {tool_name} → {result[:80]}...' if len(result) > 80 else f' Tool: {tool_name} → {result}')

            messages.append(ToolMessage(content=str(result), tool_call_id=tc['id']))

    return 'Could not complete the request within the iteration limit.'

