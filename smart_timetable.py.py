from langchain_core.tools import Tool
from datetime import datetime, time as dtime
import json



def parse_time(t: str) -> dtime:
    """Parse HH:MM string into a time object."""
    h, m = map(int, t.split(':'))
    return dtime(h, m)

def times_overlap(s1, e1, s2, e2) -> bool:
    """True if [s1,e1) overlaps [s2,e2)."""
    return parse_time(s1) < parse_time(e2) and parse_time(s2) < parse_time(e1)



def tool_view_schedule(day: str = '') -> str:
    """
    Returns the timetable. Pass a day name (e.g. 'Monday') for one day,
    or leave empty for the full week.
    """
    conn = get_conn()
    c = conn.cursor()
    if day.strip():
        rows = c.execute(
            'SELECT day,subject,start,end FROM schedule WHERE day=? ORDER BY start',
            (day.strip().title(),)
        ).fetchall()
    else:
        rows = c.execute(
            'SELECT day,subject,start,end FROM schedule ORDER BY day,start'
        ).fetchall()
    conn.close()
    if not rows:
        return 'No schedule found.'
    lines = []
    current_day = ''
    for d, subject, start, end in rows:
        if d != current_day:
            lines.append(f'\n{d}')
            current_day = d
        lines.append(f'  {start}-{end}  {subject}')
    return '\n'.join(lines)


def tool_add_event(input_str: str) -> str:
    """
    Adds a new event. Input JSON: {"day":"Monday","subject":"Study","start":"17:00","end":"18:00"}
    Returns conflict message or success.
    """
    try:
        data = json.loads(input_str)
        day     = data['day'].strip().title()
        subject = data['subject'].strip()
        start   = data['start'].strip()
        end     = data['end'].strip()
    except Exception:
        return 'Invalid input. Provide JSON: {"day":"Monday","subject":"Study","start":"17:00","end":"18:00"}'

    conn = get_conn()
    c = conn.cursor()
    existing = c.execute(
        'SELECT subject,start,end FROM schedule WHERE day=?', (day,)
    ).fetchall()

    conflicts = [
        f"{s} ({st}-{en})"
        for s, st, en in existing
        if times_overlap(start, end, st, en)
    ]

    if conflicts:
        conn.close()
        return f'❌ Conflict detected on {day} with: {', '.join(conflicts)}. Choose a different time slot.'

    c.execute('INSERT INTO schedule (day,subject,start,end) VALUES (?,?,?,?)',
              (day, subject, start, end))
    conn.commit()
    conn.close()
    return f'✅ Event "{subject}" added on {day} from {start} to {end}.'


def tool_find_free_slots(day: str) -> str:
    """
    Finds free time slots on a given day between 08:00 and 20:00.
    Input: day name, e.g. 'Monday'.
    """
    day = day.strip().title()
    conn = get_conn()
    c = conn.cursor()
    rows = c.execute(
        'SELECT start,end FROM schedule WHERE day=? ORDER BY start', (day,)
    ).fetchall()
    conn.close()

    SCHOOL_START = '08:00'
    SCHOOL_END   = '20:00'
    occupied = rows
    free = []
    cursor = SCHOOL_START

    for start, end in occupied:
        if parse_time(cursor) < parse_time(start):
            free.append(f'{cursor} – {start}')
        cursor = max(cursor, end)

    if parse_time(cursor) < parse_time(SCHOOL_END):
        free.append(f'{cursor} – {SCHOOL_END}')

    if not free:
        return f'No free slots found on {day}.'
    return f'Free slots on {day}:\n' + '\n'.join(f'  • {s}' for s in free)


def tool_view_assignments(_: str = '') -> str:
    """
    Lists all pending assignments sorted by deadline.
    """
    conn = get_conn()
    c = conn.cursor()
    rows = c.execute(
        'SELECT subject,deadline,priority FROM assignments WHERE done=0 ORDER BY deadline'
    ).fetchall()
    conn.close()
    if not rows:
        return 'No pending assignments.'
    today = datetime.now().date()
    lines = ['Pending Assignments (sorted by deadline):']
    for subject, deadline, priority in rows:
        due = datetime.strptime(deadline, '%Y-%m-%d').date()
        days_left = (due - today).days
        urgency = '🔴' if days_left <= 3 else '🟡' if days_left <= 7 else '🟢'
        lines.append(f'  {urgency} {subject} | Due: {deadline} | Priority: {priority} | {days_left} days left')
    return '\n'.join(lines)


def tool_add_assignment(input_str: str) -> str:
    """
    Adds an assignment. Input JSON: {"subject":"Math HW","deadline":"2026-06-28","priority":"High"}
    Priority: High / Medium / Low.
    """
    try:
        data = json.loads(input_str)
        subject  = data['subject'].strip()
        deadline = data['deadline'].strip()
        priority = data.get('priority', 'Medium').strip()
        datetime.strptime(deadline, '%Y-%m-%d')  # validate
    except Exception:
        return 'Invalid input. Provide JSON: {"subject":"Math HW","deadline":"2026-06-28","priority":"High"}'

    conn = get_conn()
    conn.execute('INSERT INTO assignments (subject,deadline,priority) VALUES (?,?,?)',
                 (subject, deadline, priority))
    conn.commit()
    conn.close()
    return f' Assignment "{subject}" added with deadline {deadline} (Priority: {priority}).'


def tool_suggest_study_plan(_: str = '') -> str:
    """
    Suggests a study plan by matching pending assignments to free slots this week.
    """
    conn = get_conn()
    c = conn.cursor()
    assignments = c.execute(
        'SELECT subject,deadline,priority FROM assignments WHERE done=0 ORDER BY deadline'
    ).fetchall()
    conn.close()

    if not assignments:
        return 'No pending assignments – nothing to plan!'

    days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday']
    plan = [' Suggested Study Plan (based on free slots):']
    for i, (subject, deadline, priority) in enumerate(assignments):
        day = days[i % len(days)]
        free = tool_find_free_slots(day)
        plan.append(f'\n   {subject} (due {deadline}, {priority} priority)')
        plan.append(f'     → Suggested day: {day}')
        first_free = [l for l in free.split('\n') if '•' in l]
        if first_free:
            plan.append(f'     → First free slot: {first_free[0].replace("  • ", "")}')
    return '\n'.join(plan)


